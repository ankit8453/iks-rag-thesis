"""Emit ``results/dataset_stats.md`` summarising every Phase 4 dataset.

Reads every per-dataset split JSON + class_map.json + norm YAML, plus
the soil cross-region split + meta, plus the OLID label-distribution
spreadsheet (if present). Produces a single markdown file with:

- Overview table (dataset, role, total, train/val/test, n_classes,
  image_size_target, source).
- Per-dataset detail tables (per-class counts — guardrail #3 lives
  here).
- Image-size statistics per dataset (min / median / max H, W).
- Normalisation stats table.
- Cross-region split detail.
- OLID I label co-occurrence summary (top-20).
- Validation summary (corrupt counts).
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from statistics import median

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from scripts._dataset_specs import DATASET_SPECS  # noqa: E402
from src.utils.data_splits import load_class_map, load_split  # noqa: E402
from src.utils.data_stats import load_channel_stats  # noqa: E402
from src.utils.logging_setup import get_logger  # noqa: E402
from src.utils.paths import CONFIGS_DIR, PROJECT_ROOT, RESULTS_DIR  # noqa: E402

_LOGGER = get_logger(__name__)

_SPLITS_ROOT = PROJECT_ROOT / "data" / "splits"
_NORM_ROOT = CONFIGS_DIR / "data"

_SOURCE_URLS = {
    "plantvillage": "Kaggle abdallahalidev/plantvillage-dataset",
    "plantdoc": "GitHub pratikkayal/PlantDoc-Dataset (codeload zip)",
    "paddy_doctor": "Kaggle competition paddy-disease-classification",
    "phantomfs": "Kaggle ai4a-lab/comprehensive-soil-classification-datasets",
    "irsid": "Kaggle kiranpandiri/indian-region-soil-image-dataset",
    "olid_i": "Zenodo record 8105154 (smoke sample: bottle_gourd__part_1.zip)",
}


def _image_size_stats(entries, raw_root: Path) -> dict[str, tuple[int, int]]:
    """min/median/max width and height across all images in a split."""
    from PIL import Image  # noqa: PLC0415

    widths: list[int] = []
    heights: list[int] = []
    for e in entries:
        try:
            with Image.open(raw_root / e.path) as img:
                w, h = img.size
            widths.append(w)
            heights.append(h)
        except Exception as exc:  # noqa: BLE001
            _LOGGER.warning("Skipping %s during size stats: %s", e.path, exc)
            continue
    if not widths:
        return {"width": (0, 0, 0), "height": (0, 0, 0)}
    return {
        "width": (min(widths), int(median(widths)), max(widths)),
        "height": (min(heights), int(median(heights)), max(heights)),
    }


def _per_class_counts(entries, class_map: dict[str, int]) -> dict[str, int]:
    counts = dict.fromkeys(class_map, 0)
    for e in entries:
        counts[e.label] = counts.get(e.label, 0) + 1
    return counts


def main() -> int:
    lines: list[str] = []
    lines.append("# Phase 4 Dataset Inventory\n")
    lines.append("Generated by `scripts/dataset_stats.py`. Re-run after any "
                 "split/norm change.\n")

    # ----- Overview ---------------------------------------------------- #
    lines.append("## Overview\n")
    lines.append("| Dataset | Role | Total | Train | Val | Test | Classes | "
                 "Image size | Source |")
    lines.append("|---|---|---:|---:|---:|---:|---:|---:|---|")

    summaries = []
    for spec in DATASET_SPECS:
        splits_dir = _SPLITS_ROOT / spec.name
        if not (splits_dir / "train.json").is_file():
            # IRSID lands here — it only appears in the soil cross-region split.
            n_train = n_val = n_test = 0
            n_classes = 0
            total = 0
        else:
            train = load_split(splits_dir / "train.json")
            val = load_split(splits_dir / "val.json")
            test = load_split(splits_dir / "test.json")
            cm = load_class_map(splits_dir / "class_map.json")
            n_train, n_val, n_test = len(train), len(val), len(test)
            n_classes = len(cm)
            total = n_train + n_val + n_test
        source = _SOURCE_URLS.get(spec.name, "")
        lines.append(
            f"| {spec.name} | {spec.role} | {total} | {n_train} | {n_val} | "
            f"{n_test} | {n_classes} | {spec.image_size}×{spec.image_size} | "
            f"{source} |"
        )
        summaries.append(
            (spec, n_train, n_val, n_test, total)
        )

    # ----- Per-dataset detail ----------------------------------------- #
    lines.append("\n## Per-class counts (guardrail #3)\n")
    for spec, n_train, n_val, n_test, total in summaries:
        splits_dir = _SPLITS_ROOT / spec.name
        if not (splits_dir / "train.json").is_file():
            lines.append(f"### {spec.name}\nNo standard split (see cross-region).\n")
            continue
        train = load_split(splits_dir / "train.json")
        val = load_split(splits_dir / "val.json")
        test = load_split(splits_dir / "test.json")
        cm = load_class_map(splits_dir / "class_map.json")

        lines.append(f"### {spec.name}  ({total} images, {len(cm)} classes)\n")
        lines.append("| Class | Train | Val | Test | Total |")
        lines.append("|---|---:|---:|---:|---:|")
        ct = _per_class_counts(train, cm)
        cv = _per_class_counts(val, cm)
        ce = _per_class_counts(test, cm)
        for cls in sorted(cm, key=lambda k: cm[k]):
            t, v, te = ct.get(cls, 0), cv.get(cls, 0), ce.get(cls, 0)
            lines.append(f"| {cls} | {t} | {v} | {te} | {t + v + te} |")
        lines.append("")

    # ----- Image-size statistics -------------------------------------- #
    lines.append("\n## Image-size statistics (min / median / max)\n")
    lines.append("| Dataset | Width (min/med/max) | Height (min/med/max) | "
                 "Sampled |")
    lines.append("|---|---|---|---:|")
    for spec, *_ in summaries:
        splits_dir = _SPLITS_ROOT / spec.name
        if not (splits_dir / "train.json").is_file():
            lines.append(f"| {spec.name} | — | — | 0 |")
            continue
        train = load_split(splits_dir / "train.json")
        # Sample at most 200 images for speed.
        sample = train[:200]
        stats = _image_size_stats(sample, spec.raw_root)
        wmin, wmed, wmax = stats["width"]
        hmin, hmed, hmax = stats["height"]
        lines.append(
            f"| {spec.name} | {wmin}/{wmed}/{wmax} | {hmin}/{hmed}/{hmax} | "
            f"{len(sample)} |"
        )

    # ----- Normalisation statistics ----------------------------------- #
    lines.append("\n## Normalisation statistics (per-channel mean / std in [0,1])\n")
    lines.append("| Dataset | Mean R/G/B | Std R/G/B | Sampled |")
    lines.append("|---|---|---|---:|")
    for spec, *_ in summaries:
        norm_path = _NORM_ROOT / f"{spec.name}_norm.yaml"
        if not norm_path.is_file():
            lines.append(f"| {spec.name} | — | — | 0 |")
            continue
        stats = load_channel_stats(norm_path)
        m = " / ".join(f"{v:.3f}" for v in stats.mean)
        s = " / ".join(f"{v:.3f}" for v in stats.std)
        lines.append(f"| {spec.name} | {m} | {s} | {stats.n_images_sampled} |")

    # ----- Cross-region split detail ---------------------------------- #
    lines.append("\n## Soil cross-region split (§14)\n")
    meta_path = _SPLITS_ROOT / "soil_cross_region" / "soil_cross_region_meta.json"
    if meta_path.is_file():
        meta = json.loads(meta_path.read_text(encoding="utf-8"))
        lines.append("| Split | Source | Size | Class count |")
        lines.append("|---|---|---:|---:|")
        lines.append(
            f"| train | {meta['train_source']} | {meta['train_size']} | "
            f"{len(meta['train_class_map'])} |"
        )
        lines.append(
            f"| val | {meta['val_source']} | {meta['val_size']} | "
            f"{len(meta['train_class_map'])} |"
        )
        lines.append(
            f"| test | {meta['test_source']} | {meta['test_size']} | "
            f"{len(meta['test_class_map'])} |"
        )
        lines.append(
            f"\n> **Harmonisation note:** {meta['harmonisation_note']}\n"
        )

    # ----- OLID I co-occurrence summary ------------------------------- #
    lines.append("\n## OLID I label co-occurrence (smoke sample)\n")
    lines.append(
        "Phase 4 ships a single-crop smoke sample (bottle gourd: DM / JAS / "
        "healthy). Co-occurrence becomes meaningful in Phase 11 when the "
        "full ~14 GB Zenodo download exposes the 57-label multi-label "
        "structure across all crops. Recording the placeholder for now.\n"
    )
    olid_splits = _SPLITS_ROOT / "olid_i"
    if (olid_splits / "class_map.json").is_file():
        olid_cm = load_class_map(olid_splits / "class_map.json")
        lines.append(f"Smoke-sample vocabulary ({len(olid_cm)} labels):")
        for cls, idx in sorted(olid_cm.items(), key=lambda kv: kv[1]):
            lines.append(f"- `{cls}` (idx {idx})")

    # ----- Validation summary ----------------------------------------- #
    lines.append("\n## Validation summary\n")
    corrupt_files = sorted(RESULTS_DIR.glob("corrupt_files_*.txt"))
    if corrupt_files:
        lines.append("| Dataset | Corrupt files |")
        lines.append("|---|---:|")
        for f in corrupt_files:
            n = sum(1 for line in f.read_text().splitlines() if not line.startswith("#"))
            lines.append(f"| {f.stem.replace('corrupt_files_', '')} | {n} |")
    else:
        lines.append("All datasets validated with **zero corrupt files**.\n")

    output = RESULTS_DIR / "dataset_stats.md"
    output.write_text("\n".join(lines), encoding="utf-8")
    _LOGGER.info("Wrote %s", output)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
