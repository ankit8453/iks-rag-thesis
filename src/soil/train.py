class SoilCheckpointManager:
    """Save/load multi-task soil checkpoints to/from the HF Hub model repo.

    Matches the contract of :class:`src.disease.train.CheckpointManager`
    so the Phase 6 notebook reads naturally to anyone who saw Phase 5.

    All HF Hub interactions are lazy — instantiation does NOT touch the
    network. The repo is created (private) on the first call to
    :meth:`save`, mirroring the no-side-effects-on-import contract from
    Phase 5.
    """

    def __init__(
        self,
        repo_id: str = DEFAULT_MODEL_REPO,
        work_dir: Path | None = None,
    ) -> None:
        self.repo_id = repo_id
        self.work_dir = (
            Path(work_dir) if work_dir is not None else Path(".") / "_checkpoints" / "soil"
        )
        self.work_dir.mkdir(parents=True, exist_ok=True)

    def _api(self):
        from huggingface_hub import HfApi  # noqa: PLC0415

        return HfApi()

    def _ensure_repo(self) -> None:
        self._api().create_repo(
            repo_id=self.repo_id,
            repo_type="model",
            private=True,
            exist_ok=True,
        )

    def try_load_latest(self) -> dict[str, Any] | None:
        """Pull ``checkpoint_latest.pt`` from HF Hub. ``None`` on 404."""
        from huggingface_hub import hf_hub_download  # noqa: PLC0415
        from huggingface_hub.errors import (  # noqa: PLC0415
            EntryNotFoundError,
            RepositoryNotFoundError,
        )
        import torch  # noqa: PLC0415

        try:
            local_path = hf_hub_download(
                repo_id=self.repo_id,
                filename="checkpoint_latest.pt",
                repo_type="model",
                cache_dir=str(self.work_dir / ".hf_cache"),
            )
        except (RepositoryNotFoundError, EntryNotFoundError):
            return None
        except Exception as exc:  # noqa: BLE001
            _LOGGER.warning("HF Hub load failed: %s", exc)
            return None
        return torch.load(local_path, map_location="cpu", weights_only=False)

    def save(
        self,
        *,
        epoch: int,
        model_state: dict[str, Any],
        optimizer_state: dict[str, Any] | None,
        scheduler_state: dict[str, Any] | None,
        scaler_state: dict[str, Any] | None,
        history: list[dict[str, Any]],
        val_metrics: dict[str, Any],
    ) -> Path:
        """Push ``checkpoint_latest.pt`` and ``history.json`` to HF Hub."""
        import torch  # noqa: PLC0415

        self._ensure_repo()
        local_path = self.work_dir / "checkpoint_latest.pt"
        torch.save(
            {
                "epoch": int(epoch),
                "model_state": model_state,
                "optimizer_state": optimizer_state,
                "scheduler_state": scheduler_state,
                "scaler_state": scaler_state,
                "history": list(history),
                "val_metrics": dict(val_metrics),
            },
            local_path,
        )
        self._upload(local_path, "checkpoint_latest.pt")

        history_path = self.work_dir / "history.json"
        with history_path.open("w", encoding="utf-8") as fh:
            json.dump(history, fh, indent=2)
        self._upload(history_path, "history.json")
        return local_path

    def save_best(
        self,
        *,
        epoch: int,
        model_state: dict[str, Any],
        val_metrics: dict[str, Any],
    ) -> Path:
        """Push ``checkpoint_best.pt`` separately."""
        import torch  # noqa: PLC0415

        self._ensure_repo()
        best_path = self.work_dir / "checkpoint_best.pt"
        torch.save(
            {
                "epoch": int(epoch),
                "model_state": model_state,
                "val_metrics": dict(val_metrics),
            },
            best_path,
        )
        self._upload(best_path, "checkpoint_best.pt")
        return best_path

    def _upload(self, local_path: Path, repo_path: str) -> None:
        self._api().upload_file(
            path_or_fileobj=str(local_path),
            path_in_repo=repo_path,
            repo_id=self.repo_id,
            repo_type="model",
        )
        _LOGGER.info(
            "Pushed %s -> %s/%s", local_path.name, self.repo_id, repo_path,
        )


